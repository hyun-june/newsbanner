// 뉴스 컨테이너 요소를 가져옵니다.
const newsContainer = document.getElementById("newsContainer");

// 각각의 뉴스 아이템에 대해 반복하여 카드를 생성합니다.
newsData.forEach(news => {
  // 카드 컬럼 요소를 생성합니다.
  const cardColumn = document.createElement("div");
  cardColumn.classList.add("col-md-2"); // 각 카드는 가로로 6개씩 배치합니다.

  // 카드 요소를 생성합니다.
  const cardDiv = document.createElement("div");
  cardDiv.classList.add("card");

  // 이미지 요소를 생성하고 속성을 설정합니다.
  const image = document.createElement("img");
  image.src = news.imgUrl;
  image.classList.add("card-img-top");
  image.alt = news.title;

  // 카드 본문을 생성합니다.
  const cardBody = document.createElement("div");
  cardBody.classList.add("card-body");

  // 제목 요소를 생성하고 설정합니다.
  const title = document.createElement("h5");
  title.classList.add("card-title");
  title.textContent = news.title;

  // 내용 요소를 생성하고 설정합니다.
  const content = document.createElement("p");
  // content.classList.add("card-text");
  // content.textContent = news.content;

  // 클릭 시 해당 기사로 이동하는 링크를 생성합니다.
  const articleLink = document.createElement("a");
  articleLink.href = news.source;
  articleLink.classList.add("stretched-link");
  // 카드를 클릭했을 때 해당 기사로 이동하도록 설정합니다.
  cardDiv.addEventListener("click", () => {
    window.location.href = news.source;
  });

  // 카드 본문에 제목과 내용을 추가합니다.
  cardBody.appendChild(title);
  cardBody.appendChild(content);

  // 카드에 이미지와 본문을 추가합니다.
  cardDiv.appendChild(image);
  cardDiv.appendChild(cardBody);
  cardDiv.appendChild(articleLink); // 링크를 카드에 추가합니다.

  // 카드 컬럼에 카드를 추가합니다.
  cardColumn.appendChild(cardDiv);

  // 뉴스 컨테이너에 카드 컬럼을 추가합니다.
  newsContainer.appendChild(cardColumn);
});
